    /**
     * Returns the region's abbreviation based on the time zone id provided.
     *
     * @param timeZoneId Id of time zone.
     * @return Region's abbreviation of the time zone.
     */
    public static String getTimeZoneAbbreviation(@NonNull String timeZoneId) {
        String timeZoneAbbr;
        if (android.os.Build.VERSION.SDK_INT >= android.os.Build.VERSION_CODES.N) {
            try {
                timeZoneAbbr = android.icu.util.TimeZone.getRegion(timeZoneId);
                if (!TextUtils.isEmpty(timeZoneAbbr)) {
                    return timeZoneAbbr;
                }
            } catch (IllegalArgumentException e) {
                // id is not a known system ID.
            }
        }
        timeZoneAbbr = TIME_ZONES.get(timeZoneId);
        if (!TextUtils.isEmpty(timeZoneAbbr)) {
            return timeZoneAbbr;
        } else {
            return TimeZone.getTimeZone(timeZoneId).getDisplayName(false, TimeZone.SHORT);
        }
    }

